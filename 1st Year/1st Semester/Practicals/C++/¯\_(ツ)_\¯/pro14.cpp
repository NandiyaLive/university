#include<iostream>
#include<cmath>
using namespace std;
int main ()
{int a=7;

//increment operator

cout<<"a="<<a<<endl;
cout<<"a++="<<a++<<endl;

cout<<"a="<<a<<endl;
cout<<"++a="<<++a<<endl;

 a=7;
cout<<"a="<<a<<endl;
cout<<"a--="<<a--<<endl;
cout<<"a="<<a<<endl;
cout<<"--a="<<--a<<endl;






return 0;




}




